// Translation function
function translateSubscriptionPage(lang) {
    const translations = subscriptionTranslations[lang] || subscriptionTranslations.en;

    // Update page title
    document.title = translations['page.title'];

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[key]) {
            element.textContent = translations[key];
        }
    });

    // Update the selected date text in date picker if it's visible
    const selectedDateElement = document.getElementById('selectedDate');
    if (selectedDateElement) {
        const currentKey = selectedDateElement.getAttribute('data-i18n');
        if (currentKey && translations[currentKey]) {
            selectedDateElement.textContent = translations[currentKey];
        }
    }

    // Save language preference
    localStorage.setItem('preferredLanguage', lang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Get language from localStorage or default to English
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';

    // Update active language button
    document.querySelectorAll('.lang-option').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === savedLang) {
            btn.classList.add('active');
        }
    });

    // Apply translation
    translateSubscriptionPage(savedLang);

    // Add language selector event listeners
    document.querySelectorAll('.lang-option').forEach(button => {
        button.addEventListener('click', function() {
            const selectedLang = this.getAttribute('data-lang');

            // Update active state
            document.querySelectorAll('.lang-option').forEach(btn => {
                btn.classList.remove('active');
            });
            this.classList.add('active');

            // Translate page
            translateSubscriptionPage(selectedLang);
        });
    });
});
